#파이썬 데이타 타입 알아보기
x=34
y=5
print("x=",x)
print("y=",y)
print("x+y=",x+y)
print("x-y=",x-y)
print("x*y=",x*y)
print("x/y=",x/y)#결과가 실수형으로 나온다
print("x//y=",x//y)#결과가 정수형으로 나온다(소숫점 이하 제거)
print("x%y=",x%y)#나머지 출력
print("-x",-x)#-만 붙이면 음수로 출력
print("+x",+x)
print("x**y=",x**y)
print("2**3=",2**3)#**는 지수승 구하기 2의3승

#논리형
print("x==y",x==y)#동등 연산자 false
print("x!=y",x!=y)#같지 않다 True

print("x==y",int(x==y))#동등 0
print("x!=y",int(x!=y))#같지 않다 1